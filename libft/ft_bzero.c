#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	while (n)
	{
		if (n >= 8)
		{
			*(long *)s &= 0x00;
			s += (n -= 8) ? 8 : 8;
		}
		else if (n >= 4)
		{
			*(int *)s &= 0x00;
			s += (n -= 4) ? 4 : 4;
		}
		else
		{
			*(char *)s &= 0x00;
			s += (n -= 1) ? 1 : 1;
		}
	}
}
