#include "libft.h"

void	ft_print_byte(t_byte mybyte)
{
	int i;

	i = 128;
	while (i)
	{
		if (mybyte & i)
			ft_putchar('1');
		else
			ft_putchar('0');
		i >>= 1;
	}
}
